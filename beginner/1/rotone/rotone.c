#include <unistd.h>

int		main(int c, char **v)
{
	char	*ptr;
	if (c == 2)
	{
		ptr = v[1];
		while (*ptr)
		{
			if ((*ptr >= 'A' && *ptr <= 'Y') || (*ptr >= 'a' && *ptr <= 'y'))
				(*ptr)++;
			else if (*ptr == 'Z' || *ptr == 'z')
				*ptr -= 25;
			ptr++;
		}
		write(1, v[1], ptr - v[1]);
	}
	write(1, "\n", 1);
	return (0);
}
