#include <unistd.h>

int main(int c, char **v) {
	int i;

	if (c == 4 && *v[2] && !v[2][1] && *v[3] && !v[3][1])
	{
		i = 0;
		while (v[1][i])
		{
			if (v[1][i] == v[2][0])
				v[1][i] = v[3][0];
			i++;
		}
	}
	write(1, v[1], i);
	return (0);
}
