#include <unistd.h>

int		main(int c, char **v)
{
	char *len;

	if (c > 1)
	{
		len = v[c - 1];
		while (*len)
			len++;
		write(1, v[c - 1], len - v[c - 1]);
	}
	write(1, "\n", 1);
	return (0);
}
