#include <unistd.h>

int		main(int c, char **v)
{
	if (c != 2)
		write(1, "a", 1);
	else
	{
		while (*v[1] && *v[1] != 'a')
			v[1]++;
		if (*v[1])
			write(1, "a", 1);
	}
	write(1, "\n", 1);
	return (0);
}
